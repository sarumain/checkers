package checkers;

public class Piece {
	
	boolean isPieceRed;
	int X;
	int Y;
	
	public Piece() {
		this.isPieceRed = isPieceRed;
		this.X = X;
		this.Y = Y;
	}

	boolean isPieceRed() {
		return isPieceRed;
	}

	void setPieceRed(boolean isPieceRed) {
		this.isPieceRed = isPieceRed;
	}
	
	
}
